package Zadatak2;

public class Racun {

    private String imeVlasinka;
    private double stanjeRacuna;

    public Racun(String imeVlasinka, double stanjeRacuna) {
        this.imeVlasinka = imeVlasinka;
        this.stanjeRacuna = stanjeRacuna;
    }

    public String getImeVlasinka() {
        return imeVlasinka;
    }

    public void setImeVlasinka(String imeVlasinka) {
        this.imeVlasinka = imeVlasinka;
    }

    public double getStanjeRacuna() {
        return stanjeRacuna;
    }

    public void setStanjeRacuna(double stanjeRacuna) {
        this.stanjeRacuna = stanjeRacuna;
    }


}

